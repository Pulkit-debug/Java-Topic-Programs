package OOPS.Encapsulation;

public class Encapsulation_Intro {
	
	
	// Java Encapsulation : Encapsulation in java is a process of wrapping code and data into a single unit.
	
//	Two Typs
//	1. Packages
//	2. Access Modifiers
	
	// Packages and Access Modifiers enables us to achieve encapsulation
	
	/*		Why Encapsulation ?
	 * 1. In java encapsulation helps us to keep related fields and methods together, which makes our code cleaner and easy to read.
	 * 
	 * 2. It helps to control modification of data fields.	(We can restrict things)
	 * 
	 * 3. It helps to decouple components of a system. These decoupled components can be developed, tested and debugged independly 
	 * and concurrently. And, any changes in particular component do not have any effect in their components.
	 * */
	
	
	/*
	 * 			How to achieve Encapsulation
	 * 1. Declare the variables of a class as private.
	 * 
	 * 2. Provide setter and getter methods that will be used to write and read these variable values.
	 * 
	 * 3. And you are good to go just make object in class and call get or set whatever you want .
	 */
	
	
	
	/*		Data Hiding VS Encapsulation
	 * 
	 * Encapsulation refers to the bundling of related fields and methods together. This allows us to achieve data hiding. 
	 * Encapsulation in itself is not data hiding. 	
	 * 
	 * 
	 * */
	
}
