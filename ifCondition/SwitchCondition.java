package ifCondition;

public class SwitchCondition {
	public static void main(String[] args ) {
		
		// Switch condition 
		int dayOfweek = 6;
		
	switch (dayOfweek) {
	
	case 1:
		System.out.println("Today I play PUBG");
		break; 
	case 2: 
		System.out.println("Today I do Back");
		break;
	case 3: 
		System.out.println("Today I do Chest");
	case 6:
		System.out.println(" Today I do Legs");
		break;
	default:
		System.out.println("Today I am in Pain");
		break;
	
	}
		
	}	
}
