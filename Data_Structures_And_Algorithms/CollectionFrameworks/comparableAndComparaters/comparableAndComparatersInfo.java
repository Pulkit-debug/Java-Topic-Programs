	// ****************************** Comparable Interface *********************
	
//	The interface imposes a total ordering on the objects of each class that implements it. This ordering is referred to as the class's
//	natural ordering, and the class's compareTo method is referred to as it's natural comparison method.
//	
//	Lists ( and arrays) of objects that implements this interface can be stored automatically by collections.sort (and Arrays.sort).
//	
//	Objects that implement this interface can be used as keys in a sorted map or as elements in a sorted set, without the need to specify 
//	a comparator.
//	
//	// Structure: 
//	
//	public interface Comparable<T> {
//		public int compareTo(T o);
//	}
	
	
	// ******************************* Comparator Interface ******************
	
//	A comparison function which imposes a total ordering on some collections on objects.
//	
//	Comparators can be passed to a sort method (such as collections.sort or Arrays.sort) to allow precise control over the sort order
//	
//	Comparatos can also be used to conrol the order of certain data structures (such as sorted sets or sorted maps), or to provide
//	an ordering for collections of objects that don't have a natural ordering.
	
	
	
